import java.util.Scanner;


public class HotTub {
	private int temp;
	private int numJets;
	private int size;
	
	public HotTub(int temp, int numJets, int size) {
		super();
		this.temp = temp;
		this.numJets = numJets;
		this.size = size;
	}
	public void print(){
		System.out.println("temp:" + temp );
		System.out.println("numJets: " + numJets);
		System.out.println("Size: " + size);
	}
	/**
	 * @param args
	 */


}
